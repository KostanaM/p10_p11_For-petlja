package kostana.maksic;

public class Zadatak5 {

	public static void main(String[] args) {

		// 5. Napisati niz naredbi kojim se izračunava kvadrat prirodnih brojeva od 15
		// do 25.
		int n;
		for (int k = 15; k <= 25; k++) {

			n = k * k;
			System.out.println("Kvadrat broja " + k + " iznosi: " + n);
		}

	}

}
